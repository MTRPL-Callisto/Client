
import UIKit

extension UIColor {
    static var main: UIColor {
        return UIColor(named: "Main")!
    }

    static var highlight: UIColor {
        return UIColor(named: "Highlight")!
    }

    static var shade: UIColor {
        return UIColor(named: "Shade")!
    }

    static var backgroundDark: UIColor {
        return UIColor(named: "BackgroundDark")!
    }

    static var backgroundLight: UIColor {
        return UIColor(named: "BackgroundLight")!
    }
}
